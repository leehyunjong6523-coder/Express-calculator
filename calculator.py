    ups_imp_zi       = ups_imp_zone_num - 1

    # ── 외곽지역 ──
    # ── 외곽지역 수수료 동적 계산: max(35,000, 750×청구중량) ──
    def calc_remote_sur(chargeable_wt):
        return max(DHL_REMOTE_SUR_MIN, ceil10(DHL_REMOTE_SUR_KG * chargeable_wt))

    is_remote = check_dhl_remote_area(dest_country, remote_postal, remote_city)

    # ── 국가 부가수수료 ──
    _dhl_country_surs = {}
    if dest_country in DHL_CONFLICT_COUNTRIES:
        _dhl_country_surs["분쟁지역 배송"] = DHL_CONFLICT_SUR
    if dest_country in DHL_SANCTION_COUNTRIES:
        _dhl_country_surs["무역제재국 배송"] = DHL_SANCTION_SUR
    if is_remote:
        _dhl_country_surs["외곽지역(RAS)"] = calc_remote_sur(0)  # 중량 확정 후 아래서 갱신

    # ── 프라임워터 특별 할인 ──
    _is_pw = any(p in customer.lower().replace(" ", "") for p in ["프라임워터", "primewater"])
    if _is_pw:
        disc_dhl = 63.0 if dhl_zone >= 7 else (60.0 if dhl_zone == 5 else 55.0)

    # ── 5% 할증 체크 ──
    _dhl_5pct = "5%" in customer

    # p_add (DHL 남부 Zone 추가)
    p_add = 0

    # ── C/T 루프 ──
    sur_dhl_ct   = dict(_dhl_country_surs)
    sur_fedex_ct = {}
    sur_ups_ct   = {}

    total_actual_wt  = 0
    total_vol_wt     = 0
    total_chargeable = 0

    for ct in ct_data:
        winfo = calc_weight(ct["wt"], ct["L"], ct["W"], ct["H"])
        total_actual_wt  += ct["wt"] * ct.get("qty", 1)
        total_vol_wt     += winfo["volume"] * ct.get("qty", 1)
        total_chargeable += winfo["rounded"] * ct.get("qty", 1)
        dims = sorted([ct["L"], ct["W"], ct["H"]], reverse=True)
        w = winfo["rounded"]

        # DHL surs
        s_dhl = {}
        if 25 < w <= 70: s_dhl["컨베이어 운반 불가"] = 30000
        if w > 70:        s_dhl["중량 초과"] = 150000
        if dims[0] > 100 or dims[1] > 80: s_dhl["길이 초과"] = 30000
        for k, v in s_dhl.items(): sur_dhl_ct[k] = sur_dhl_ct.get(k, 0) + v * ct.get("qty", 1)

        # FedEx surs
        s_fx = {}
        if w > 50:
            s_fx["특대형"] = 86000
        else:
            if w > 25:              s_fx["추가취급(중량)"] = 35600
            if dims[0] > 121 or dims[1] > 76: s_fx["추가취급(용적)"] = 35600
        for k, v in s_fx.items(): sur_fedex_ct[k] = sur_fedex_ct.get(k, 0) + v * ct.get("qty", 1)

        # UPS surs
        s_ups = {}
        girth = (dims[1] + dims[2]) * 2
        if (dims[0] + girth) > 300:
            s_ups["대형포장물"] = 69200
        elif dims[0] > 122 or dims[1] > 76 or ct["wt"] > 25:
            s_ups["비규격품(OSP)"] = 21400
        if ct["wt"] > 70: s_ups["__freight__"] = 1
        for k, v in s_ups.items(): sur_ups_ct[k] = sur_ups_ct.get(k, 0) + v * ct.get("qty", 1)

    _total_w = total_chargeable   # ← 루프 종료 후 즉시 확정

    # ── 캐리어별 청구중량 (반올림 기준 상이) ──
    _wt_dhl   = round_wt_dhl(_total_w)
    _wt_fedex = round_wt_fedex(_total_w)
    _wt_ups   = round_wt_ups(_total_w)

    total_sur_dhl   = sum(v for k, v in sur_dhl_ct.items() if k != "__freight__")

    # 외곽지역 수수료 중량 확정 후 재계산 (max(35,000, 750×청구중량))
    if is_remote:
        actual_remote_sur = calc_remote_sur(_total_w)
        if "외곽지역(RAS)" in sur_dhl_ct:
            total_sur_dhl = total_sur_dhl - sur_dhl_ct["외곽지역(RAS)"] + actual_remote_sur
            sur_dhl_ct["외곽지역(RAS)"] = actual_remote_sur
    total_sur_fedex = sum(sur_fedex_ct.values())

    # FedEx 초과수요 추가요금 (2026-03-05~, kg당 × 청구중량)
    _eds = get_fedex_eds(fx_zone, _total_w)
    if _eds > 0:
        sur_fedex_ct["초과수요(EDS)"] = _eds
        total_sur_fedex += _eds

    # FedEx ODA/OPA 추가요금
    _fx_tier = get_fedex_oda_opa_tier(dest_country, remote_postal, remote_city)
    _fx_oda_opa = calc_fedex_oda_opa_sur(_fx_tier, _total_w)
    if _fx_oda_opa["opa"] > 0:
        sur_fedex_ct["서비스외지역 픽업(OPA)"] = _fx_oda_opa["opa"]
        total_sur_fedex += _fx_oda_opa["opa"]
    if _fx_oda_opa["oda"] > 0:
        sur_fedex_ct["서비스외지역 배송(ODA)"] = _fx_oda_opa["oda"]
        total_sur_fedex += _fx_oda_opa["oda"]
    total_sur_ups   = sum(v for k, v in sur_ups_ct.items() if k != "__freight__")
    ups_freight_flag = sur_ups_ct.get("__freight__", 0) > 0

    # UPS 외곽지역 서비스(EAS)
    # 수출: 목적지 배송(delivery)만 적용 / 수입: 픽업(pickup)만 적용
    _ups_eas = get_ups_eas(dest_country, remote_postal, remote_city)
    _ups_eas_sur = calc_ups_eas_sur(_ups_eas, _total_w)
    if mode == "수출":
        if _ups_eas_sur["delivery"] > 0:
            sur_ups_ct["외곽지역 배송(EAS)"] = _ups_eas_sur["delivery"]
            total_sur_ups += _ups_eas_sur["delivery"]
    else:  # 수입
        if _ups_eas_sur["pickup"] > 0:
            sur_ups_ct["외곽지역 픽업(EAS)"] = _ups_eas_sur["pickup"]
            total_sur_ups += _ups_eas_sur["pickup"]

    # ── DHL ──
    if mode == "수출":
        total_pub_dhl, total_net_dhl, dhl_rpk = dhl_lookup(_total_w, dhl_zi, is_doc)
        _p_add_total = p_add * len(ct_data)
        total_pub_dhl += _p_add_total
        total_net_dhl += _p_add_total
    else:
        total_pub_dhl = dhl_imp_lookup(_total_w, dhl_zi, is_doc)
        total_net_dhl = dhl_imp_cost_lookup(_total_w, dhl_zi, is_doc)
        dhl_rpk = None

    # ── FedEx ──
    if mode == "수출":
        total_pub_fedex_ip, fx_rpk   = fedex_lookup(_total_w, fx_zi, is_doc=is_doc, econ=False)
        total_net_fedex_ip  = ceil10(total_pub_fedex_ip * 0.5)
        total_pub_fedex_ec, fx_ec_rpk = fedex_lookup(_total_w, fx_zi, is_doc=False, econ=True)
        total_net_fedex_ec  = ceil10(total_pub_fedex_ec * 0.5)
    else:
        total_pub_fedex_ip, fx_rpk   = fximp_lookup(_total_w, fx_zi, is_doc=is_doc, econ=False)
        total_net_fedex_ip  = ceil10(total_pub_fedex_ip * 0.5)
        total_pub_fedex_ec, fx_ec_rpk = fximp_lookup(_total_w, fx_zi, is_doc=False, econ=True)
        total_net_fedex_ec  = ceil10(total_pub_fedex_ec * 0.5)

    # ── UPS ──
    if mode == "수출":
        total_pub_ups_2F, total_net_ups_2F, ups_rpk = ups_lookup(_total_w, ups_zi, is_doc, "2F94A8")
        total_pub_ups_B8, total_net_ups_B8, _       = ups_lookup(_total_w, ups_zi, is_doc, "B8733R")
    else:
        total_pub_ups_2F = ups_imp_lookup(_total_w, ups_imp_zi, is_doc)
        total_net_ups_2F = ups_imp_cost_lookup(_total_w, ups_imp_zi, is_doc, "2F94A8")
        total_pub_ups_B8 = ups_imp_lookup(_total_w, ups_imp_zi, is_doc)
        total_net_ups_B8 = ups_imp_cost_lookup(_total_w, ups_imp_zi, is_doc, "B8733R")
        ups_rpk = None

    # ── 5% 할증 : 공시가에 5% 추가 후 10원 단위 올림 ──
    if _dhl_5pct:
        _dhl_pub_for_calc = math.ceil(total_pub_dhl * 1.05 / 10) * 10
    else:
        _dhl_pub_for_calc = total_pub_dhl

    # ── calc_carrier ──
    UPS_ACCT_FEE_RATE = 0.03  # 전체 원가 기준 3% → calc_carrier 내부에서 계산
    res_dhl     = calc_carrier("DHL Express",   _dhl_pub_for_calc,  total_net_dhl,      total_sur_dhl,   fuel_dhl,   disc_dhl)
    res_fedex   = calc_carrier("FedEx IP",      total_pub_fedex_ip, total_net_fedex_ip, total_sur_fedex, fuel_fedex, disc_fedex)
    res_fedex_e = calc_carrier("FedEx Economy", total_pub_fedex_ec, total_net_fedex_ec, total_sur_fedex, fuel_fedex, disc_fedex_e)
    res_ups2f   = calc_carrier("UPS 2F94A8",    total_pub_ups_2F,   total_net_ups_2F,   total_sur_ups,   fuel_ups,   disc_ups,   net_fee=UPS_ACCT_FEE_RATE)
    res_upsb8   = calc_carrier("UPS B8733R",    total_pub_ups_B8,   total_net_ups_B8,   total_sur_ups,   fuel_ups,   disc_ups_b8 if mode == "수입" else disc_ups, net_fee=UPS_ACCT_FEE_RATE)

    # ── 부가서비스 목록 (표시용) ──
    def _sur_list(d):
        return [{"name": k, "amount": v} for k, v in d.items() if k != "__freight__"]

    return {
        "dest_country":      dest_country,
        "dest_kr":           COUNTRY_KR.get(dest_country, dest_country),
        "mode":              mode,
        "is_doc":            is_doc,
        "dhl_zone":          dhl_zone,
        "fx_zone":           fx_zone,
        "ups_zone":          ups_zone_num,
        "is_remote":         is_remote,
        "remote_sur":        sur_dhl_ct.get("외곽지역(RAS)", 0) if is_remote else 0,
        "conflict":          dest_country in DHL_CONFLICT_COUNTRIES,
        "sanction":          dest_country in DHL_SANCTION_COUNTRIES,
        "ups_no_service":    ups_no_service,
        "fedex_no_service":  fedex_no_service,
        "ups_freight_flag":  ups_freight_flag,
        "total_actual_wt":   round(total_actual_wt, 1),
        "total_vol_wt":      round(total_vol_wt, 1),
        "total_chargeable":  round(total_chargeable, 1),
        "wt_dhl":            _wt_dhl,
        "wt_fedex":          _wt_fedex,
        "wt_ups":            _wt_ups,
        "carriers": {
            # disc_rpk = 실제 청구 항공운임 ÷ 청구중량 → 10원 단위 올림
            "dhl":     {**res_dhl,     "surs": _sur_list(sur_dhl_ct),   "fuel_pct": fuel_dhl,   "disc_pct": disc_dhl,    "surcharge_5pct": _dhl_5pct, "rpk": dhl_rpk,   "disc_rpk": ceil10(res_dhl.get("pub_disc",0)   / _wt_dhl)   if dhl_rpk   and _wt_dhl   else None, "chargeable_wt": _wt_dhl},
            "fedex":   {**res_fedex,   "surs": _sur_list(sur_fedex_ct), "fuel_pct": fuel_fedex, "disc_pct": disc_fedex,  "rpk": fx_rpk,    "disc_rpk": ceil10(res_fedex.get("pub_disc",0)  / _wt_fedex) if fx_rpk    and _wt_fedex else None, "chargeable_wt": _wt_fedex},
            "fedex_e": {**res_fedex_e, "surs": _sur_list(sur_fedex_ct), "fuel_pct": fuel_fedex, "disc_pct": disc_fedex_e,"rpk": fx_ec_rpk, "disc_rpk": ceil10(res_fedex_e.get("pub_disc",0)/ _wt_fedex) if fx_ec_rpk and _wt_fedex else None, "chargeable_wt": _wt_fedex},
            "ups2f":   {**res_ups2f,   "surs": _sur_list(sur_ups_ct),   "fuel_pct": fuel_ups,   "disc_pct": disc_ups,    "rpk": ups_rpk,   "disc_rpk": ceil10(res_ups2f.get("pub_disc",0)  / _wt_ups)   if ups_rpk   and _wt_ups   else None, "chargeable_wt": _wt_ups},
            "upsb8":   {**res_upsb8,   "surs": _sur_list(sur_ups_ct),   "fuel_pct": fuel_ups,   "disc_pct": disc_ups_b8, "rpk": ups_rpk,   "disc_rpk": ceil10(res_upsb8.get("pub_disc",0)  / _wt_ups)   if ups_rpk   and _wt_ups   else None, "chargeable_wt": _wt_ups},
        }
    }
