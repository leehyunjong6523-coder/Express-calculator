def dhl_imp_lookup(w, zi, is_doc=False):
    """DHL 수입 요금 조회 (공시가)"""
    if is_doc and w <= 2.0:
        k = math.ceil(w * 2) / 2
        return ceil10(next(DHL_IMP_DOX[key][zi] for key in sorted(DHL_IMP_DOX) if key >= k))
    elif w <= 30.0:
        k = math.ceil(w * 2) / 2
        return ceil10(next(DHL_IMP_NDC[key][zi] for key in sorted(DHL_IMP_NDC) if key >= k))
    else:
        rw = math.ceil(w)
        if rw <= 70:   br = "30-70"
        elif rw <= 300:br = "70-300"
        else:          br = "300+"
        return ceil10(DHL_IMP_OVER[br][zi] * rw)

def fximp_lookup(w, zi, is_doc=False, econ=False):
    """FedEx 수입 요금 조회 → (price, rpk_or_None)"""
    if not econ:
        if is_doc and w <= 0.5:
            return ceil10(FXIMP_ENV[0.5][zi]), None
        if is_doc and w <= 2.5:
            k = math.ceil(w * 2) / 2
            return ceil10(next(FXIMP_PAK[key][zi] for key in sorted(FXIMP_PAK) if key >= k)), None
    tbl  = FXIMP_ECON      if econ else FXIMP_IP
    over = FXIMP_ECON_OVER if econ else FXIMP_OVER
    if w <= 20.5:
        k = math.ceil(w * 2) / 2
        return ceil10(next(tbl[key][zi] for key in sorted(tbl) if key >= k)), None
    rw = int(math.ceil(w))
    if rw <= 44:    br = "21-44"
    elif rw <= 70:  br = "45-70"
    elif rw <= 99:  br = "71-99"
    elif rw <= 299: br = "100-299"
    elif rw <= 499: br = "300-499"
    elif rw <= 999: br = "500-999"
    else:           br = "1000+"
    rpk = over[br][zi]
    return ceil10(rpk * rw), rpk

