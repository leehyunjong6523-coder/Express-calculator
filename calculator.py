    if postal_code:
        pn = _re.sub(r"[\s\-]", "", str(postal_code)).strip()
        # 숫자 우편번호
        pn_num = None
        try: pn_num = int(pn)
        except: pass
        for row in postal_data:
            b, e, opa, oda = row
            # 숫자 비교 가능하면 숫자로
            try:
                if pn_num is not None and int(b) <= pn_num <= int(e):
                    return {"opa": opa, "oda": oda}
            except:
                if b <= pn <= e:
                    return {"opa": opa, "oda": oda}

