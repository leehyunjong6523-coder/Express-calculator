def get_fedex_eds(fx_zone: str, chargeable_wt: float) -> int:
    """FedEx 초과수요 추가요금 — kg당 요금 × 청구중량 (1원 올림)"""
    if not _FEDEX_EDS_ENABLED:
        return 0
    rate = _FEDEX_EDS_EXPORT.get(fx_zone, 0)
    if rate == 0:
        return 0
    return math.ceil(rate * chargeable_wt)
